#working with assignment operators by user input 
#input the values of a and b
a = int(input("Enter the value of a: "))
b = int(input("Enter the value of b: "))

# = operator
print("The value of a =", a, "and b =", b)

# += operator
a += b
print("The value of a +=", a)

# -= operator
a -= b
print("The value of a -=", a)

# *= operator
a *= b
print("The value of a *=", a)

# /= operator
a /= b
print("The value of a /=", a)

# **= operator
a **= b
print("The value of a **=", a)

# %= operator
a %= b
print("The value of a %=", a)

# //= operator
a //= b
print("The value of a //=",a)