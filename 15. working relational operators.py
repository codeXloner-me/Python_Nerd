#working with relational operators by user input 
#input the values of a and b
a = int(input("Enter the value of a: "))
b = int(input("Enter the value of b: "))

# == operators
print("== checks that a is equal to b\n",a==b)

# != operators
print("!= checks that a is not equal to b\n",a!=b)

# > operators
print("> checks that a is greater than b\n",a>b)

# < operators
print("< checks that a is less than b\n",a<b)

# >= operators
print(">= checks that a is greater than or equal to b\n",a>=b)

# <= operators
print("<= checks that a is less than or equal to b\n",a<=b)

